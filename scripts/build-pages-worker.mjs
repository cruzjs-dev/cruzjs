#!/usr/bin/env node
/**
 * Build script for Cloudflare Pages with React Router SSR
 *
 * Creates a _worker.js file that wraps the React Router server build
 * for Cloudflare Pages Functions with nodejs_compat
 */
import { writeFileSync, copyFileSync, existsSync, mkdirSync } from 'fs';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = resolve(__dirname, '..');
const distClient = resolve(rootDir, 'dist/client');
const distServer = resolve(rootDir, 'dist/server');

console.log('Creating _worker.js for Cloudflare Pages...\n');

// Ensure client directory exists
if (!existsSync(distClient)) {
  console.error('Error: dist/client not found. Run build first.');
  process.exit(1);
}

// Check server build exists
if (!existsSync(resolve(distServer, 'index.js'))) {
  console.error('Error: dist/server/index.js not found. Run build first.');
  process.exit(1);
}

// Copy server bundle to client directory so it's included in Pages deployment
const serverBundleSrc = resolve(distServer, 'index.js');
const serverBundleDest = resolve(distClient, '_server.js');
copyFileSync(serverBundleSrc, serverBundleDest);
console.log('Copied server bundle to:', serverBundleDest);

// Copy server assets (CSS) if they exist
const serverAssetsDir = resolve(distServer, 'assets');
const clientAssetsDir = resolve(distClient, 'assets');
if (existsSync(serverAssetsDir)) {
  const { readdirSync } = await import('fs');
  const files = readdirSync(serverAssetsDir);
  for (const file of files) {
    if (file.endsWith('.css')) {
      copyFileSync(resolve(serverAssetsDir, file), resolve(clientAssetsDir, file));
      console.log('Copied server CSS:', file);
    }
  }
}

// Create the _worker.js content
const workerContent = `
// Cloudflare Pages Worker for React Router SSR
// Generated by build-pages-worker.mjs
import { createRequestHandler } from "@react-router/cloudflare";

// Import the server build (copied to _server.js)
import * as serverBuild from "./_server.js";

// Create the request handler
// createRequestHandler returns a PagesFunction that expects EventContext
const requestHandler = createRequestHandler({
  build: serverBuild,
  mode: "production"
});

export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);

    try {
      // Only try static assets for GET requests (POST/PUT/etc need their body intact)
      if (request.method === "GET" && env.ASSETS) {
        try {
          const assetResponse = await env.ASSETS.fetch(request.url, {
            headers: request.headers,
          });
          if (assetResponse.status >= 200 && assetResponse.status < 400) {
            return assetResponse;
          }
        } catch {}
      }

      // Construct EventContext-like object that createRequestHandler expects
      const context = {
        request,
        env,
        waitUntil: ctx.waitUntil.bind(ctx),
        passThroughOnException: ctx.passThroughOnException?.bind(ctx) || (() => {}),
      };

      return await requestHandler(context);
    } catch (error) {
      console.error("[Worker] Error:", error.message || error);
      console.error("[Worker] Stack:", error.stack || 'no stack');
      return new Response("Internal Server Error", { status: 500 });
    }
  }
};
`.trim();

// Write _worker.js to client directory
const workerPath = resolve(distClient, '_worker.js');
writeFileSync(workerPath, workerContent);
console.log('Created:', workerPath);

// Also copy the server assets if they exist
const serverAssets = resolve(distServer, 'assets');
if (existsSync(serverAssets)) {
  console.log('Note: Server assets are in dist/server/assets');
}

console.log('\nDone! Ready for deployment with:');
console.log('  npm run aurora cloudflare:deploy --skip-build');
